package bgu.spl.net.api;

public class Pair {

    private String H;
    private String K;

    public Pair(String h, String k) {
        H = h;
        K = k;
    }

    public String getH() {
        return H;
    }

    public String getK() {
        return K;
    }

    public void setH(String h) {
        H = h;
    }

    public void setK(String k) {
        K = k;
    }
}
